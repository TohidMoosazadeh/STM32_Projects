/* USER CODE BEGIN 1 */
void HAL_SPI_TxRxCpltCallback(SPI_HandleTypeDef *hspi)  // The master activates the slave.
{
  if(hspi == &hspi1)
  {
    spiRxFlag = 1;
    HAL_GPIO_WritePin(GPIOA, GPIO_PIN_4, GPIO_PIN_SET);
  }
}

void HAL_ADC_ConvCpltCallback(ADC_HandleTypeDef *hadc)
{
  uint16_t tmp = HAL_ADC_GetValue(&hadc1);
  adcRawValue[0] = (tmp & 0xFF00) >> 8;
  adcRawValue[1] = tmp & 0xFF; 
}

void HAL_SPI_TxCpltCallback(SPI_HandleTypeDef *hspi)
{
  if(hspi == &hspi2)
    HAL_SPI_Transmit_IT(&hspi2, (uint8_t *)adcRawValue, 2);
}
