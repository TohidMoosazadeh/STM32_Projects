/* USER CODE BEGIN 1 */
void HAL_I2C_MasterRxCpltCallback(I2C_HandleTypeDef *hi2c)
{
  rxflag_i2c = 1;
}

void HAL_I2C_ListenCpltCallback(I2C_HandleTypeDef *hi2c)
{
  HAL_I2C_EnableListen_IT(&hi2c2);
}

void HAL_I2C_AddrCallback(I2C_HandleTypeDef *hi2c, uint8_t TransferDirection, uint16_t AddrMatchCode)
{
  if(TransferDirection == I2C_DIRECTION_RECEIVE) // if the master wants to recieve the data
  {
    HAL_I2C_Slave_Seq_Transmit_IT(hi2c, (uint8_t *)txBuffer_i2c, 15, I2C_FIRST_AND_LAST_FRAME);
  }
}

void HAL_I2C_ErrorCallback(I2C_HandleTypeDef *hi2c)
{
  uint32_t errorcode = HAL_I2C_GetError(&hi2c2);
  if(errorcode == HAL_I2C_ERROR_BERR)
  {
    HAL_I2C_DeInit(&hi2c2);
    HAL_I2C_Init(&hi2c2);
  }
  HAL_I2C_EnableListen_IT(&hi2c2);
}
